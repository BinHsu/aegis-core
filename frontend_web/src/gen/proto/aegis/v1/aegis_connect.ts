// Load-bearing design notes (DO NOT change without a new ADR):
//   * Raw audio PCM lives ONLY in the Engine process RAM for the
//     lifetime of a single StreamTranscribe stream. See ADR-0005
//     (seven enforcement requirements). Aegis does NOT process
//     voiceprint / biometric data at all — see ADR-0012.
//   * Transcript segments are never persisted server-side. The Go Gateway
//     is a stateless fan-out relay; see ADR-0004.
//   * Speaker labels in transcripts may be anonymous (Speaker_0..N) or
//     role-based (Host, Client, Colleague). Never real names; see
//     ARCHITECTURE.md §9.2.
//   * Viewer access control is invite-token-based for MVP. The
//     `allowed_viewer_account_ids` field on CreateMeetingRequest is
//     RESERVED for the Phase 5+ enterprise tier per ADR-0001 "Future
